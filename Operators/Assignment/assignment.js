// Assignment Operators
// Equal =
var a = 10;
var b = a;
a+b;
console.log(a)

// Plus Equal +=
var c = 10;
var d = 25;
c+=d; 
// c=c+d;
console.log(c)

// Minus Equal -=
var e = 10;
var f = 25;
e-=f; 
// e=e-f;
console.log(e)

// Multiply Equal *=
var g = 5;
var h = 25;
g*=h; 
// g=g*h;
console.log(g)

// devide Equal /=
var i = 10;
var j = 25;
i/=j; 
// i=i/j;
console.log(i)

// reminder Equal /=
var k = 10;
var  l = 25;
k%=l; 
// k=k%l;
console.log(k)
