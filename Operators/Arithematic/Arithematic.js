// Arithematic Operation
// +    Addition
var add = 10 + 12 + 5 + 6;
console.log("add--->", add);

// -	Subtraction
var sub = 20 - 5 - 6;
console.log("sub--->", sub);

// *	Multiplication
var mult = 4 * 2 * 2;
console.log("mult--->", mult);

// **	Exponentiation 
var exp = 2 ** 16;
console.log("exp--->", exp);

// /	Division
var div = 10 / 3;
console.log("div--->", div);

// %	Modulus (Remainder)
var mod = 20 % 3;
console.log("mod--->", mod);

// ++	Increment
var inc = 20;
inc++;
// inc=inc+1
// inc=inc+3
console.log("inc--->", inc);

// --	Decrement
var dec = 20;
// dec++;
// dec=dec-1
dec=dec-3
console.log("dec--->", dec);


